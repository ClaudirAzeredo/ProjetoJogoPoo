package br.unipar.jogo;

public class ModeloPersonagem {

        private Integer sexo;

        private Integer andar;

        private Integer correr;

        private Integer pular;

        private Integer agachar;

        private Integer deitar;

        private Integer rastejar;

    public Integer getSexo() {
        return sexo;
    }

    public void setSexo(Integer sexo) {
        this.sexo = sexo;
    }

    public Integer getAndar() {
        return andar;
    }

    public void setAndar(Integer andar) {
        this.andar = andar;
    }

    public Integer getCorrer() {
        return correr;
    }

    public void setCorrer(Integer correr) {
        this.correr = correr;
    }

    public Integer getPular() {
        return pular;
    }

    public void setPular(Integer pular) {
        this.pular = pular;
    }

    public Integer getAgachar() {
        return agachar;
    }

    public void setAgachar(Integer agachar) {
        this.agachar = agachar;
    }

    public Integer getDeitar() {
        return deitar;
    }

    public void setDeitar(Integer deitar) {
        this.deitar = deitar;
    }

    public Integer getRastejar() {
        return rastejar;
    }

    public void setRastejar(Integer rastejar) {
        this.rastejar = rastejar;
    }
}
